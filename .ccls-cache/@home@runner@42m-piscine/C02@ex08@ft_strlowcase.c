#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	int	m;

	m = 0;
	while (str[m] != '\0')
	{
		if (str[m] >= 'A' && str[m] <= 'Z')
		{
			str[m] += 32;
		}
		m++;
	}
	return (str);
}