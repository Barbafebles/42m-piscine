#include <unistd.h>

char	*ft_strcpy(char *dest, char *src)
{
	int	m;

	m = 0;
	while (src[m] != '\0')
	{
		dest[m] = src[m];
			m++;
	}
	dest[m] = '\0';
	return (dest);
}