char	*ft_strcat(char *dest, char *src)
{
	int	m;
	int	x;

	x = 0;
	m = 0;
	while (dest[m] != '\0')
		m++;
	while (src[x] != '\0')
	{
		dest[m + x] = src[x];
		x++;
	}
	dest [m + x] = '\0';
	return (dest);
}